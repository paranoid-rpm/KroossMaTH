// ====== ИНИЦИАЛИЗАЦИЯ ВКЛАДОК ТЕОРИИ ======
function initTheory() {
  const classSelect = document.getElementById('classSelect');
  if (classSelect) {
    GRADES.forEach(g => {
      const opt = document.createElement('option');
      opt.value = g;
      opt.textContent = `${g} класс`;
      classSelect.appendChild(opt);
    });
    classSelect.addEventListener('change', () => {
      const activeTab = document.querySelector('.theory-tabs .tab-btn.active');
      renderTheory(activeTab ? activeTab.dataset.tab : 'why', classSelect.value);
    });
  }

  document.querySelectorAll('.theory-tabs .tab-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      document.querySelectorAll('.theory-tabs .tab-btn').forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      const classSelect = document.getElementById('classSelect');
      renderTheory(btn.dataset.tab, classSelect ? classSelect.value : '');
    });
  });

  renderTheory('why', '');
}

document.addEventListener('DOMContentLoaded', initTheory);
